#include "Music.h"

// ------------------------------------------------------------------------------------------------

void Music::Load(const char* file)
{
	music = std::make_unique<sf::Music>();
	music->openFromFile(file);
}

// ------------------------------------------------------------------------------------------------

void Music::Play(bool loop)
{
	if (loop)
		music->setLoop(loop);
	music->play();
}

// ------------------------------------------------------------------------------------------------

void Music::Stop()
{
	music->stop();
	music->setLoop(false);
}

// ------------------------------------------------------------------------------------------------

void Music::Volume(float volume)
{
	music->setVolume(volume);
}

